// ============================================================
// setLanguage — Apply translations, update lang attr, fire event
// ============================================================
function setLanguage(lang) {
    if (!translations[lang]) lang = 'en';

    // Save preference
    localStorage.setItem('retail_lang', lang);

    // Update html lang attribute
    document.documentElement.lang = lang;

    // Fade-out transition on body content
    const mainContent = document.getElementById('mainScrollArea');
    if (mainContent) {
        mainContent.style.transition = 'opacity 0.25s ease';
        mainContent.style.opacity = '0.6';
        setTimeout(() => { mainContent.style.opacity = '1'; }, 350);
    }

    // Traverse all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Preserve child icons if present
            const icon = el.querySelector('i');
            if (icon) {
                let span = el.querySelector('span.i18n-text');
                if (!span) {
                    el.innerHTML = el.innerHTML.replace(/(<i[^>]*><\/i>)\s*(.*)/i, '$1 <span class="i18n-text">$2</span>');
                    span = el.querySelector('span.i18n-text');
                }
                if (span) span.textContent = translations[lang][key];
                else el.textContent = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    // Handle placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) el.placeholder = translations[lang][key];
    });

    // Sync all language selectors on the page
    document.querySelectorAll('.lang-selector').forEach(sel => { sel.value = lang; });

    // Fire custom event so other modules (voice, copilot) can react
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang, config: langConfig[lang] } }));
}

// ============================================================
// Boot: read localStorage and apply saved language
// ============================================================
document.addEventListener('DOMContentLoaded', () => {
    // Default to English unless the user explicitly chose a language (via the UI selector)
    // 'retail_lang_explicit' is only set when the user manually selects a language
    const explicitLang = localStorage.getItem('retail_lang_explicit');
    const savedLang = explicitLang || 'en';

    // Sync the stored key so setLanguage stays consistent
    localStorage.setItem('retail_lang', savedLang);

    // Attach change handlers to all selectors with class lang-selector
    document.querySelectorAll('.lang-selector').forEach(sel => {
        sel.value = savedLang;
        sel.addEventListener('change', (e) => {
            // Mark as explicit user choice
            localStorage.setItem('retail_lang_explicit', e.target.value);
            setLanguage(e.target.value);
        });
    });

    setLanguage(savedLang);
});
